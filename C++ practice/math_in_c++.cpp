#include<iostream>
#include<cmath>
using namespace std;
int main()
{
    int num1 = 3;
    int num2 = 9;
    float num3 = 2.3651;

    cout<<max(num1,num2)<<"\n";//Return maximum no.
    
    cout<<min(num1,num2)<<"\n";//Return minimum no.
    
    cout<<pow(num2,num1)<<"\n";//Return num2 raise to power num1.
    
    cout<<sqrt(num2)<<"\n";//Return Square of number.
    
    cout<<round(num3)<<"\n";//Rounds a number
    
    cout<<log(num1)<<"\n";//Return log of number
    

 return 0;
}